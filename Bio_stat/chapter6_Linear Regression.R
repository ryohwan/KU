# 3. 상호작용(Interaction)
# 반응변수 Y와 두개의 독립변수 X, A가 있을 때, X가 Y에 끼치는 영향이
# A값에 따라 달라질 때 상호작용이 존재한다.
# A가 X의 효과를 수정(modify)한다고 말한다.
# 예) 다이어트 보조제 효과의 성별 차이
#  -> Y : 3개월 간 몸무게 감소량
#  -> X : 다이어트 보조제 복용 여부(1=복용, 0=복용하지 않음)
#  -> A : 성별(1=여성, 0=남성)
#  -> 성별에 따라 다이어트 보조제의 효과가 다르다 = X와 A간 상호작용이 있다.
## 상호작용이 있을 때, 분석 방법
## --> 잘못된 분석 방법
###### 데이터를 성별에 따라 나눈다.
###### 남성의 다이어트 보조제 효과를 추정한다.
###### 여성의 다이어트 보조제 효과를 추정한다.
###### 남성과 여성에서 효과의 추정값이 다르므로 치료효과가 성별에 따라 다르다고
##  --> 올바른 분석 방법
###### 상호작용항(Interaction term)을 이용하여 성별에 따라 다이어트 보조제 효과가
###### 통계적으로 유의하게 다른지 검정한다.

