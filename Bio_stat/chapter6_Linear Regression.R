# 2. 교란(confounding)
# 독립변수와 반응변수 모두에 영향을 끼치는 변수
# 실제로 독립변수가 반응변수에 영향을 끼치지 않더라도,
# 실제로는 두 변수 사이에 인과관계가 없더라도, 교란변수가 있는 경우
# 독립변수와 반응변수 간 상관관계가 생겨날 수 있다.
# 상관관계를 곧바로 인과관계로 해석하면 안되는 중요한 이유
# -> 세이터에서 상관관계가 관측될 경우, 이것이 인과관계 때문에 생겨난 상관관계인지,
# -> 교란변수 때문에 생겨난 상관관계인지 확인하여 해석에 반영해야 한다.
# 교란변수가 있을 때 어떻게 분석을 할까?
# 교란변수를 다루는 방법
# -> 랜덤화(Randomization) : 독립변수의 값을 랜덤하게 부여함.
# -> 층화(Stratification) : 교란변수의 값에 따라 데이터를 여러 층으로 나누어, 각 층마다 독립변수와 반응변수의 상관관계를 따로 추정
# -> 매칭된 환자-대조군 연구(matched case-control study) :
# -> 조정(adjustment) : 독립변수가 여러 개인 회귀분석을 이용하는 방법
# -> 성향점수분석(prorensity score analysis) : 교란변수의 효과가 독립변수에 끼친 영향을 추정한후,
#      이를 이용하여 관찰연구의 데이터를 교란변수의 효과가 제거된 임상시험의 데이터처럼 가공해서 분석하는 방법


