# 독립변수의 개수가 2개 이상인 선형회귀모형
# 회귀보형에 독립변수를 새로 추가할 때마다 결정계수는 항상 커진다.
# 독립변수의 개수가 다른 2개 모형의 성능을 비교할 때 결정계수를 기준으로 삼는 것은 적절하지 않다.
